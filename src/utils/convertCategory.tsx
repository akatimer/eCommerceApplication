export default function convertCategory(color: string): string | undefined {
  switch (color) {
    case '33bf067c-4760-4e2a-8921-9c4242f4f3a1':
      return 'Kids';
    case 'db9d0198-4eda-4024-8d3a-d5876a8cba01':
      return 'Women';
    case 'a40115c6-8fb2-43ba-acb1-316c39463539':
      return 'Unisex';
  }
}
